                <p>Registered as a Society of weavers, we are a conglomerate of small Self Help Groups based in the rural areas of Madhya Pradesh, India. There are 10 groups of ladies and gents who are generating livelihood through a cohesive effort for 100 families. We aim to enhance self esteem and self dependence, generate employment opportunities for people in the villages, and thus control migration to cities. We train to work under socially accountable fair wage system and defined quality parameters.</p>
                
