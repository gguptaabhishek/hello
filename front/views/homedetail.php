                <p>“To develop as a quality service organisation promoting rural livelihood, encouraging economic independence of women and complimenting ecologically friendly organisations in promotion of hand spun, hand woven natural fibres. Thus developing conscientious and responsible communities’ world wide” .<p>
                
                 
                <br><h4><b class="txcolor">Background:-</b></h4>   
                <p><b>‘Aavran handloom society’</b> is a livelihood initiative where 100 families get livelihood by producing organic hand spun hand-woven fabric. Around 70 women spinners, 25 young weavers and 25 other families are making this initiative a unique cohesive effort for self reliance.</p>   
                <p>The roots of <b>‘Aavran’</b> are closely connected to its promoting organization bioRe Association. The bioRe Association is a registered social organization working to empower organic and biodynamic communities by facilitating education and promoting infrastructure, addressing local needs thus leading to holistic and sustainable development.</p>
                <p>bioRe Association started a small initiative of promoting livelihood for its women self help groups through 10 Hand spinning machines (Amber Charkha) in Sept 2007. Possibilities of promoting hand spun hand woven organic cloth through establishing a handloom based livelihood project was observed seeing potential market and interest of SHG members, Taking this into consideration bioRe Foundation Switzerland helped bioRe Association in developing the required infrastructure. Success of the initiative was converted into a democratic institution named and registered as <b>“Aavran”</b></p> 
               
                
                <br><h4><b class="txcolor">How Aavran works:-</b></h4>   
                <p>Aavran being social enterprises believes in equality, quality, transparency, fair trade and making locally global products thus advancing with world by consolidating and valuing our traditional knowledge and culture. Aavran is managed by hand spinners, weavers and its founding members from the youth club. The Organic cotton in the form of Silver (roving cotton) is organized from bioRe India Ltd. And it is delivered to women self help groups who produces the hand spun yarn with the help of Ambar Charkhas at their home. These women self help group are given training of three months before going into production.</p>   
                <p>The hand spun yarn is transported to the Aavran handloom workshop where processes from winding to finished products are being carried out. The natural dyeing of the yarn or the cloth is done by the approved organizations.</p> 
                
                
